void
PcLogicTestGenerator::startup()
{
    DPRINTF(PcLogicTestGenerator, "Starting pc_logic tests, total %zu vectors\n", testVectors.size());
    
    for (size_t i = 0; i < testVectors.size(); i++) {
        TestVector& vec = testVectors[i];
        
        dut.setRst(vec.rst);
        dut.setCurrentPc(vec.current_pc);
        dut.setPcPlus4(vec.pc_plus4);
        dut.setBranchTarget(vec.branch_target);
        dut.setJumpTarget(vec.jump_target);
        dut.setJalrTarget(vec.jalr_target);
        dut.setIsJal(vec.is_jal);
        dut.setIsJalr(vec.is_jalr);
        dut.setBranchTaken(vec.branch_taken);
        dut.setIsException(vec.is_exception);
        dut.setExceptionAddr(vec.exception_addr);
        dut.setIsMret(vec.is_mret);
        dut.setMretAddr(vec.mret_addr);
        dut.setHoldPc(vec.hold_pc);
        dut.process();
        
        bool pass = true;
        if (dut.getPcSel() != vec.expected_pc_sel) pass = false;
        if (dut.getNextPc() != vec.expected_next_pc) pass = false;
        
        if (pass) {
            passed++;
            DPRINTF(PcLogicTestGenerator, "Test TP%03d PASSED\n", (int)(i + 1));
        } else {
            failed++;
            DPRINTF(PcLogicTestGenerator, "Test TP%03d FAILED: pc_sel=%d next_pc=0x%x, expected pc_sel=%d next_pc=0x%x\n",
                    (int)(i + 1), (int)dut.getPcSel(), dut.getNextPc(), vec.expected_pc_sel, vec.expected_next_pc);
        }
    }
    
    DPRINTF(PcLogicTestGenerator, "Tests complete: %d passed, %d failed, %d total\n",
            (int)passed, (int)failed, (int)testVectors.size());
    
    if (failed > 0) {
        panic("PcLogicTestGenerator: %d tests failed!", (int)failed);
    }
    
    exitSimLoop("PcLogicTestGenerator complete");
}

